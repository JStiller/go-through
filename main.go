package main

import "fmt"

func main() {
	// How to set variables
	var a = "hello world!"
	fmt.Println(a)

	var b int = 1
	fmt.Println(b)

	var c = true
	fmt.Println(c)

	var d int
	fmt.Println(d)

	// using shorthand for declaring and initializing a variable
	e := "hello world!"
	fmt.Println(e)

	f := 1
	fmt.Println(f)

	g := true
	fmt.Println(g)
}
